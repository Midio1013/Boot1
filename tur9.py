import turtle as t

a = t.Pen()
a.speed(0)


def pud(xe, ye):
    a.penup()
    a.goto(xe, ye)
    a.pendown()


a.penup()
a.goto(-300, 200)
a.pendown()
a.pensize(5)

a.begin_fill()
a.forward(600)
a.right(90)
a.forward(400)
a.right(90)
a.forward(600)
a.right(90)
a.forward(400)
a.fillcolor("#27E985")
a.end_fill()

a.pensize(1)
a.right(90)
pud(-300, 150)
a.forward(600)
for i in range(3):
    i *= 50
    pud(-300, 100 - i)
    a.forward(600)
pud(-300,200)
a.right(90)
pud(-200,200)
a.forward(200)
for i in range(5):
    i-=i*100
    pud(-200-i,200)
    a.forward(200)
a.left(180)
pud(-300,-200)
a.forward(250)
pud(-200,-200)
a.forward(150)
pud(-100,-200)
a.forward(150)
pud(0,-200)
a.forward(150)
pud(100,-200)
a.forward(150)
pud(200,-200)
a.forward(150)
a.left(90)
pud(300,-150)
a.forward(600)
pud(300,-100)
a.forward(600)
pud(300,-50)
a.forward(600)
pud(-190,160)
a.left(180)
a.write("星期一",font=("宋体",20,"normal"))
pud(-90,160)
a.write("星期二",font=("宋体",20,"normal"))
pud(10,160)
a.write("星期三",font=("宋体",20,"normal"))
pud(110,160)
a.write("星期四",font=("宋体",20,"normal"))
pud(210,160)
a.write("星期五",font=("宋体",20,"normal"))
pud(-290,110)
a.write("第一节",font=("宋体",20,"normal"))
pud(-290,60)
a.write("第二节",font=("宋体",20,"normal"))
pud(-290,10)
a.write("第三节",font=("宋体",20,"normal"))
pud(-290,-90)
a.write("第四节",font=("宋体",20,"normal"))
pud(-290,-140)
a.write("第五节",font=("宋体",20,"normal"))
pud(-290,-190)
a.write("第六节",font=("宋体",20,"normal"))


t.done()
