import turtle as t
a=t.Pen()
a.pensize(3)
a.penup()
a.goto(-150,100)
a.pendown()

a.pencolor("yellow")
a.begin_fill()
a.forward(300)
a.left(135)
a.forward(212.1)
a.left(90)
a.forward(212.1)
a.fillcolor("#9EA216")
a.end_fill()

a.pencolor("#26F40B")
a.left(45)
a.begin_fill()
for i in range(4):
    a.forward(300)
    a.left(90)
a.fillcolor("#22B70F")
a.end_fill()

a.penup()
a.goto(0,150)
a.pendown()

a.left(180)
a.pencolor("red")
a.begin_fill()
a.forward(50)
a.left(90)
a.circle(25,-180)
a.fillcolor("#F8820A")
a.end_fill()

a.penup()
a.goto(35,-200)
a.pendown()
a.pencolor("#0AF8DA")

a.begin_fill()
a.left(180)
a.forward(70)
a.right(90)
a.forward(130)
a.right(90)
a.forward(70)
a.right(90)
a.forward(130)
a.fillcolor('#1D8D7F')
a.end_fill()

a.penup()
a.goto(100,0)
a.pendown()

a.left(180)
a.begin_fill()
a.circle(30)
a.fillcolor("#6ADA9C")
a.end_fill()
a.left(90)
a.forward(60)
a.backward(30)
a.right(90)
a.forward(30)
a.backward(60)

a.penup()
a.goto(-100,0)
a.pendown()



t.done()